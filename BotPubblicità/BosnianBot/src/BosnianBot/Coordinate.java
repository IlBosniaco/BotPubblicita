package BosnianBot;

/**
 *
 * @author matte
 */
public class Coordinate {
    float lat;
    float lon;
    
    public Coordinate(){
        lat=0;
        lon=0;
    }

    public Coordinate(float lat, float lon) {
        this.lat = lat;
        this.lon = lon;
    }

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLon() {
        return lon;
    }

    public void setLon(float lon) {
        this.lon = lon;
    }
    
    
}
